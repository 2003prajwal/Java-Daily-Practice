class Array5 {
    public static void main(String[] args) {
        int[] myArray = {1,4,53,5,3,7,9,3,74,78,45,25,};
        //Array Traversal Method
        int index = 1;
        while (index < myArray.length){
            System.out.println(myArray[index]);
            index++;
        }
    }
}
