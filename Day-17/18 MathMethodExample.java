class MathMethodExample {
    public static void main(String[] args) {
        System.out.println(Math.min(12,31));
        int a=7, b=14;
        System.out.println(Math.max(a,b));

        System.out.println(Math.sqrt(23));

        System.out.println(Math.pow(2,3));  //2^3 = 2 × 2 × 2 = 8

        System.out.println(Math.abs(-35)); // remove minus sign

        int x=10, y=20;
        double avg =(x+y)/2.0;
        System.out.println(avg);

       // ❌ Math.avg() does NOT exist
    }
}
